import { AGIAlgorithm } from 'agi-algorithm-library';

class AGIAlgorithm {
  constructor() {
    this.agiAlgorithm = new AGIAlgorithm();
  }

  run(data) {
    // Run AGI algorithm on analyzed data
    const decision = this.agiAlgorithm.run(data);
    return decision;
  }
}

export default AGIAlgorithm;
